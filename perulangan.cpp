#include <iostream>
using namespace std;

int main ()
{
  int a;
  cout << "Contoh Program Perulangan Menurun 1-10" << endl;
  for (a=1; a<=10; a++)
  cout << a << "\n";
  getchar ();

  cout << "Perulangan Menurun 10-1" << endl;
  for (a=10; a>=1; a--)
  cout << a << "\n";
  getchar ();
}